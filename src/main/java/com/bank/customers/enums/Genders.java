package com.bank.customers.enums;

/**
 * Enum representing the gender of a customer in the banking system.
 */
public enum Genders {
    /**
     * Indicates that the gender is not specified or unknown.
     */
    UNKNOWN,

    /**
     * Represents a male customer.
     */
    MALE,

    /**
     * Represents a female customer.
     */
    FEMALE
}
