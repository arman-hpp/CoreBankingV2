package com.bank.accounts.enums;

/**
 * Enum representing the type of account in the banking system.
 */
public enum AccountTypes {
    /**
     * Indicates that the account type is not specified or unknown.
     */
    UNKNOWN,

    /**
     * Represents an account belonging to a customer for personal or business transactions.
     */
    CUSTOMER_ACCOUNT,

    /**
     * Represents an internal bank account used for operational or financial management.
     */
    BANK_ACCOUNT
}